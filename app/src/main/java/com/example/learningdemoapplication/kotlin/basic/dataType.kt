package com.example.learningdemoapplication.kotlin.basic

fun main() {
    //INt
    var age: Int = 20
    println(age)

    //Byte(8 bits = 1 bit)

    val byte: Byte = 20
    println(byte)

    //short (16 bits  =  2 bit)
    val short: Short = 123
    println(short)

    //int (32 bits = 4 bit -> 2 * 31   -- 2 *31 -1
    val int: Int = 142
    println(int)

    //long(64 bits = 8 bit)
    val long: Long = 15515151576767L
    println(long)

    //flot(32 bits  = 4 bit )
    val float: Float = 12.222222F
    println(float)

    //double(64 bits = 8 bit)
    val double: Double = 1333.333333333333333333333
    println(double)
    //char
    val char:Char='s'
    println(char)

    //string daata type
    val name:String="Savai"
    println(name)

    //boolean
    var trueData:Boolean=false
    println(trueData)
}